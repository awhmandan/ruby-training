weather = 'windy'
case weather
when 'sunny'
  puts "Something light!"
when 'rainy'
  puts "Something with lots of hoods"
when 'foggy'
  puts "Not sure, but bring a torch!"
when 'windy'
  puts "Lead boots!"
when 'snowy'
  puts "Layers and layers and layers"
else
  puts "Not sure, follow your heart!"
end